use crate::println;

pub fn print_logo() {
    let logo = r#"
    ____  __  ___________  ____  __    __
   / __ \/ / / /  _/__  / / __ \/ /   / / 
  / / / / / / // /   / / / / / / /   / /  
 / /_/ / /_/ // /   / /_/ /_/ / /___/ /__ 
/_____/\____/___/  /____/\____/_____/____/

    "#;

    for line in logo.lines() {
        println!("{}", line);
    }
}